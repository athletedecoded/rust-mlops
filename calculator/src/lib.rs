/* A simple calculator */

// Add function
pub fn add(x: f64, y: f64) -> f64 {
    x + y
}

// Subtract function
pub fn subtract(x: f64, y: f64) -> f64 {
    x - y
}

// Floor function
pub fn divide(x: f64, y: f64) -> f64 {
    x / y
}

// Multiply function
pub fn multiply(x: f64, y: f64) -> f64 {
    x * y
}
