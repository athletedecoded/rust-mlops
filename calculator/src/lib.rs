/* A simple calculator */

// Add function
pub fn add(a: f64, b: f64) -> f64 {
    a + b
}

// Subtract function
pub fn subtract(a: f64, b: f64) -> f64 {
    a - b
}

// Divide function
pub fn divide(a: f64, b: f64) -> f64 {
    a / b
}

// Multiply function
pub fn multiply(a: f64, b: f64) -> f64 {
    a * b
}
